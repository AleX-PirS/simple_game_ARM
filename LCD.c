void InitTimer(void){
	MDR_TIMER1->ARR = 0;
	MDR_TIMER1->PSG = 1;
}

void RST_CLK_Init(void){
	MDR_RST_CLK->PER_CLOCK |= 0xFFFFFFFF;                
	MDR_RST_CLK->HS_CONTROL |= RST_CLK_HS_CONTROL_HSE_ON;
	while(!(MDR_RST_CLK->CLOCK_STATUS & RST_CLK_CLOCK_STATUS_HSE_RDY));
	
	MDR_RST_CLK->CPU_CLOCK |= (0x2<<RST_CLK_CPU_CLOCK_CPU_C1_SEL_Pos)|(RST_CLK_CPU_CLOCK_CPU_C2_SEL) ;

	MDR_RST_CLK->PLL_CONTROL |= (RST_CLK_PLL_CONTROL_PLL_CPU_ON);
	MDR_RST_CLK->PLL_CONTROL |= (RST_CLK_PLL_CONTROL_PLL_CPU_PLD);
	MDR_RST_CLK->PLL_CONTROL |= (4<<RST_CLK_PLL_CONTROL_PLL_CPU_MUL_Pos);
	MDR_RST_CLK->PLL_CONTROL &= ~(RST_CLK_PLL_CONTROL_PLL_CPU_PLD);
	while(!(MDR_RST_CLK->CLOCK_STATUS & RST_CLK_CLOCK_STATUS_PLL_CPU_RDY));

	MDR_RST_CLK->CPU_CLOCK |= (0x1<<RST_CLK_CPU_CLOCK_HCLK_SEL_Pos);
	

	MDR_RST_CLK->TIM_CLOCK |= (RST_CLK_TIM_CLOCK_TIM1_CLK_EN);
	
}

void Delay(uint32_t t){
	MDR_TIMER1->PSG = 9;
	MDR_TIMER1->ARR = t+1;
	MDR_TIMER1->CNT = 1;
	MDR_TIMER1->STATUS = 0;
	MDR_TIMER1->CNTRL |= TIMER_CNTRL_CNT_EN;
	
	while (((MDR_TIMER1->STATUS) & TIMER_STATUS_CNT_ARR_EVENT) == 0)
	{}
		
	MDR_TIMER1->CNTRL &= ~(TIMER_CNTRL_CNT_EN);
}

void WriteByteLCD(uint8_t CMD, uint8_t Crystal, uint32_t Data){
	if (CMD==command)
	{
		MDR_PORTE -> RXTX &= ~(1<<A0);
	}
	else 
	{
		MDR_PORTE -> RXTX |= (1<<A0);
	}
	Delay(TIME_STEP);
	if (Crystal==left)
	{
		MDR_PORTE -> RXTX |= (1<<E1);
		Delay(TIME_STEP);
		MDR_PORTE -> RXTX &= ~(1<<E2);
	}
	else 
	{
		MDR_PORTE -> RXTX |= (1<<E2);
		Delay(TIME_STEP);
		MDR_PORTE -> RXTX &= ~(1<<E1);
	}
	Delay(TIME_STEP);
	*adr = Data;
	Delay(TIME_STEP);
}

uint8_t ReadByteLCD(uint8_t CMD, uint8_t Crystal){
	if (CMD==command)
	{
		MDR_PORTE -> RXTX &= ~(1<<A0);
	}
	else 
	{
		MDR_PORTE -> RXTX |= (1<<A0);
	}
	
	Delay(TIME_STEP);
	
	if (Crystal==left)
	{
		MDR_PORTE -> RXTX |= (1<<E1);
		Delay(TIME_STEP);
		MDR_PORTE -> RXTX &= ~(1<<E2);
	}
	else 
	{
		MDR_PORTE -> RXTX |= (1<<E2);
		Delay(TIME_STEP);
		MDR_PORTE -> RXTX &= ~(1<<E1);
	}
	return *adr;
}
	
void InitLCD(void){
	MDR_EBC->CONTROL |= (EBC_CONTROL_WAIT_STATE_Msk) | EBC_CONTROL_RAM;
	MDR_PORTE->RXTX &= ~(1<<RST);
	Delay(TIME_BIG_STEP);
	MDR_PORTE->RXTX |= (1<<RST);
	Delay(TIME_BIG_STEP);
	WriteByteLCD(command, left, cmdDisplayOn);
	WriteByteLCD(command, right, cmdDisplayOn);
}

void DrawLCD(void){
	for (uint32_t i = 0; i<63; i++)
	{
	WriteByteLCD (data, left, i);
	}
}

void SetPose(uint8_t Page, uint8_t adrs){
	if( adrs > 63)
	{
		WriteByteLCD(command, right, cmdSetPage+Page);
		WriteByteLCD(command, right, cmdSetAdr+adrs-64);
	}
	else
	{
		WriteByteLCD(command, left, cmdSetPage+Page);
		WriteByteLCD(command, left, cmdSetAdr+adrs);
	}
	
}

void WriteBytePos(uint8_t adrs, uint8_t Data){
	if( adrs > 63)
	{
		WriteByteLCD(data, right, Data);
	}
	else
	{
		WriteByteLCD(data, left, Data);
	}
}

void ClrScreen (void){
	for (uint8_t i=0; i<8; i++)
	{
		for (uint8_t k=0; k<128; k++)
		{
			SetPose(i, k);
			WriteBytePos(k, 0x0); 
		}
	}
}
