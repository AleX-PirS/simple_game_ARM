uint8_t DetectPageSkip(uint8_t *parameters, uint8_t relative_row){
	uint8_t minus = parameters[3] + relative_row;
	uint8_t page = 0;
	if (minus > 7){
		while(minus > 7){
			minus -= 8;
			page++;
		}
		return page;
	}
	return 0; 
}

uint8_t DetectRow(uint8_t value, uint8_t column_param){
	uint8_t num_iter_row = 0;
	while (value >= column_param){
		value = value - column_param;
		num_iter_row += 1;
	}
	return num_iter_row;
}

uint8_t DetectCol(uint8_t value, uint8_t row_param, uint8_t column_param){
	uint8_t relative_column = 0;
	for (uint8_t z = 0; z < row_param; z++){
		for (uint8_t j = 0; j < column_param; j++){
			if ((z * column_param + j) == value){
				relative_column = j;
				return relative_column;
			}
		}
	}
	return 0;
}

